import click

from airbyte.cli.cloud._api_helpers import get_cloud_workspace, job_to_dict
from airbyte.cli.cloud._json_helpers import (
    JsonHelpCommand,
    JsonHelpGroup,
    error_json,
    json_output,
    register_schema,
    resolve_entity_id,
)


@click.group(cls=JsonHelpGroup)
@click.pass_context
def jobs(ctx: click.Context) -> None:
    """View Airbyte sync jobs."""
