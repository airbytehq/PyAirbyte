register_schema(
    "jobs_list",
    description="List recent sync jobs for a connection.",
    required_params={
        "workspace_id": "The workspace ID.",
        "connection_id": "The connection ID.",
    },
    optional_params={"limit": "Maximum number of jobs to return (default: 20)."},
)


@jobs.command("list", cls=JsonHelpCommand)
@click.option("--connection-id", default=None, help="The connection ID to list jobs for.")
@click.option("--limit", default=20, help="Maximum number of jobs to return.")
@click.pass_context
def jobs_list(ctx: click.Context, connection_id: str | None, limit: int) -> None:
    """List recent jobs for a connection."""
    if not connection_id:
        error_json("--connection-id is required.", type="MissingParameter")
    assert connection_id is not None
    workspace = get_cloud_workspace(ctx)
    results = workspace.get_connection(connection_id).get_previous_sync_logs(limit=limit)
    json_output([job_to_dict(j) for j in results])


register_schema(
    "jobs_get",
    description="Get details of a specific job by ID.",
    required_params={"job_id": "The job ID to retrieve, as an argument or --job-id."},
)


@jobs.command("get", cls=JsonHelpCommand)
@click.argument("job_id_arg", required=False, type=int)
@click.option("--job-id", default=None, type=int, help="The job ID to retrieve.")
@click.pass_context
def jobs_get(ctx: click.Context, job_id_arg: int | None, job_id: int | None) -> None:
    """Get details of a specific job."""
    job_id = int(
        resolve_entity_id(
            str(job_id_arg) if job_id_arg is not None else None,
            str(job_id) if job_id is not None else None,
            option_name="--job-id",
        )
    )
    workspace = get_cloud_workspace(ctx)
    result = workspace.get_job_info(job_id)
    json_output(job_to_dict(result))


register_schema(
    "jobs_wait",
    description="Wait for a job to reach a terminal status.",
    required_params={"job_id": "The job ID to wait for, as an argument or --job-id."},
    optional_params={"wait_timeout": "Maximum seconds to wait for job completion."},
)


@jobs.command("wait", cls=JsonHelpCommand)
@click.argument("job_id_arg", required=False, type=int)
@click.option("--job-id", default=None, type=int, help="The job ID to wait for.")
@click.option(
    "--wait-timeout",
    default=300,
    type=int,
    help="Maximum seconds to wait for job completion.",
)
@click.pass_context
def jobs_wait(
    ctx: click.Context, job_id_arg: int | None, job_id: int | None, wait_timeout: int
) -> None:
    """Wait for a job to complete."""
    job_id = int(
        resolve_entity_id(
            str(job_id_arg) if job_id_arg is not None else None,
            str(job_id) if job_id is not None else None,
            option_name="--job-id",
        )
    )
    workspace = get_cloud_workspace(ctx)
    result = workspace.get_connection(workspace.get_job_info(job_id).connection_id).get_sync_result(
        job_id
    )
    assert result is not None
    result.wait_for_completion(wait_timeout=wait_timeout)
    json_output(job_to_dict(result))


